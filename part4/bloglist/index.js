const express = require('express')
const app = express()
const cors = require('cors')
const mongoose = require('mongoose')

const blogSchema = new mongoose.Schema({
  title: String,
  author: String,
  url: String,
  likes: Number
})

const Blog = mongoose.model('Blog', blogSchema)

const mongodbPassword = process.env.MONGODB_PASSWORD
const mongodbUsername = process.env.MONGODB_USER
const mongodbHostname = process.env.MONGODB_HOST
const mongodbDBName = process.env.MONGODB_DB_NAME

const mongoUrl = `mongodb+srv://${mongodbUsername}:${mongodbPassword}@${mongodbHostname}/${mongodbDBName}?retryWrites=true`

mongoose.connect(mongoUrl, { useNewUrlParser: true, useUnifiedTopology: true, useFindAndModify: false, useCreateIndex: true })

app.use(cors())
app.use(express.json())

app.get('/api/blogs', (request, response) => {
  Blog
    .find({})
    .then(blogs => {
      response.json(blogs)
    })
})

app.post('/api/blogs', (request, response) => {
  const blog = new Blog(request.body)

  blog
    .save()
    .then(result => {
      response.status(201).json(result)
    })
})

const PORT = 3003
app.listen(PORT, () => {
  console.log(`Server running on port ${PORT}`)
})
